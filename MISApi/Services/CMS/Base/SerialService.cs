        /// <summary>
        /// 默认查询
        /// </summary>
        /// <param name="context"></param>
        /// <param name="joins"></param>
        /// <returns></returns>
        protected IQueryable<SQLEntity> SQLQueryable(PandoraContext context, params BaseMode.Join[] joins)
        {
            try
            {
                // 定义
                var left = context.CMS_Serial.Select(Main => new SQLEntity
                {
                    Serial = Main
                });
                // 遍历
                foreach (var join in joins)
                {
                    // SQLEntity.Payer
                    if (join.Name.ToLower().Equals("payer"))
                    {
                        left = left.LeftOuterJoin(context.CMS_Member, Main => Main.Serial.PayerId, Left => Left.Id, (Main, Left) => new SQLEntity
                        {
                            Serial = Main.Serial,
                            Payer = Left,
                            Receiver = Main.Receiver,
                            Status = Main.Status
                        });
                    }
                    // SQLEntity.Receiver
                    if (join.Name.ToLower().Equals("receiver"))
                    {
                        left = left.LeftOuterJoin(context.CMS_Member, Main => Main.Serial.ReceiverId, Left => Left.Id, (Main, Left) => new SQLEntity
                        {
                            Serial = Main.Serial,
                            Payer = Main.Payer,
                            Receiver = Left,
                            Status = Main.Status
                        });
                    }
                    // SQLEntity.Status
                    if (join.Name.ToLower().Equals("status"))
                    {
                        left = left.LeftOuterJoin(context.WFM_Status, Main => Main.Serial.StatusId, Left => Left.Id, (Main, Left) => new SQLEntity
                        {
                            Serial = Main.Serial,
                            Payer = Main.Payer,
                            Receiver = Main.Receiver,
                            Status = Left
                        });
                    }
                }
                // 一对多
                var group = left.Select(Main => new SQLEntity
                {
                    Serial = Main.Serial,
                    Payer = Main.Payer,
                    Receiver = Main.Receiver,
                    Status = Main.Status
                });
                // 遍历
                foreach (var join in joins)
                {

                }
                // 返回
                return group;
            }
            catch(Exception ex)
            {
                throw new Exception("MISApi.Services.CMS.Base.SerialService.SQLQueryable", ex);
            }
        }
        /// <summary>
        /// 关键字
        /// </summary>
        /// <param name="queryable"></param>
        /// <param name="keyWord"></param>
        /// <returns></returns>
        protected IQueryable<SQLEntity> KeyWordQueryable(IQueryable<SQLEntity> queryable, string keyWord)
        {
            try
            {
                // #分割生成and数组
                string[] ands = keyWord.Split('#');
                // 空格分割生成or数组
                string[] ors = Regex.Split(keyWord, "\\s+", RegexOptions.IgnoreCase);
                // 多个条件精确查询，单个条件关键字查询
                if (ands.Length > 1)
                {
                    for (var i = 0; i < ands.Length; i++)
                    {
                        var andKeyWord = ands[i];
                        queryable = queryable.Where(row =>
                                row.Serial.SerialNo.Contains(andKeyWord) ||
                                row.Serial.PayerName.Contains(andKeyWord) ||
                                row.Serial.PayerRealName.Contains(andKeyWord) ||
                                row.Serial.ReceiverName.Contains(andKeyWord) ||
                                row.Serial.ReceiverRealName.Contains(andKeyWord) ||
                                row.Serial.Remark.Contains(andKeyWord) ||
                                row.Serial.StatusName.Contains(andKeyWord)
                            );
                    }
                }
                else if (ors.Length > 1)
                {
                    queryable = queryable.Where(row =>
                            ors.Contains(row.Serial.SerialNo) ||
                            ors.Contains(row.Serial.PayerName) ||
                            ors.Contains(row.Serial.PayerRealName) ||
                            ors.Contains(row.Serial.ReceiverName) ||
                            ors.Contains(row.Serial.ReceiverRealName) ||
                            ors.Contains(row.Serial.Remark) ||
                            ors.Contains(row.Serial.StatusName)
                        );
                }
                else
                {
                    queryable = queryable.Where(row =>
                            row.Serial.SerialNo.Contains(keyWord) ||
                            row.Serial.PayerName.Contains(keyWord) ||
                            row.Serial.PayerRealName.Contains(keyWord) ||
                            row.Serial.ReceiverName.Contains(keyWord) ||
                            row.Serial.ReceiverRealName.Contains(keyWord) ||
                            row.Serial.Remark.Contains(keyWord) ||
                            row.Serial.StatusName.Contains(keyWord)
                        );
                }
                // 返回
                return queryable;
            }
            catch (Exception ex)
            {
                throw new Exception("MISApi.Services.CMS.Base.SerialService.KeyWordQueryable", ex);
            }
        }
        /// <summary>
        /// 关键字扩展
        /// </summary>
        /// <param name="queryable"></param>
        /// <param name="keyWordExt"></param>
        /// <returns></returns>
        protected IQueryable<SQLEntity> KeyWordExtQueryable(IQueryable<SQLEntity> queryable, string keyWordExt)
        {
            try
            {
                // 拆分查询条件
                var splits = keyWordExt.Split('^');
                // 遍历
                for (var i = 0; i < splits.Length; i++)
                {
                    if (splits[i].ToLower().Contains("payerid"))
                    {
                        int payerId = int.Parse(splits[i].Substring(splits[i].IndexOf("=") + 1, splits[i].Length - splits[i].IndexOf("=") - 1));
                        queryable = queryable.Where(row => row.Serial.PayerId == payerId);
                    }
                    if (splits[i].ToLower().Contains("receiverid"))
                    {
                        int receiverId = int.Parse(splits[i].Substring(splits[i].IndexOf("=") + 1, splits[i].Length - splits[i].IndexOf("=") - 1));
                        queryable = queryable.Where(row => row.Serial.ReceiverId == receiverId);
                    }
                    if (splits[i].ToLower().Contains("statusid"))
                    {
                        int statusId = int.Parse(splits[i].Substring(splits[i].IndexOf("=") + 1, splits[i].Length - splits[i].IndexOf("=") - 1));
                        queryable = queryable.Where(row => row.Serial.StatusId == statusId);
                    }
                    if (splits[i].ToLower().Contains("dealamount"))
                    {
                        decimal min = splits[i].IndexOf(">") > -1 ? decimal.Parse(splits[i].Substring(splits[i].IndexOf(">") + 1, splits[i].Length - splits[i].IndexOf(">") - 1)) : int.MinValue;
                        decimal max = splits[i].IndexOf("<") > -1 ? decimal.Parse(splits[i].Substring(splits[i].IndexOf("<") + 1, splits[i].Length - splits[i].IndexOf("<") - 1)) : int.MaxValue;
                        queryable = queryable.Where(row => row.Serial.DealAmount >= min && row.Serial.DealAmount <= max);
                    }
                }
                return queryable;
            }
            catch (Exception ex)
            {
                throw new Exception("MISApi.Services.CMS.Base.SerialService.KeyWordExtQueryable", ex);
            }
        }
        /// <summary>
        /// 日期
        /// </summary>
        /// <param name="queryable"></param>
        /// <param name="dates"></param>
        /// <returns></returns>
        protected IQueryable<SQLEntity> DateQueryable(IQueryable<SQLEntity> queryable, params BaseMode.Date[] dates)
        {
            try
            {
                if (dates != null)
                {
                    dates.ToList().ForEach(date =>
                    {
                        if (date.Name.ToLower().Equals("dealdatetime"))
                        {
                            queryable = queryable
                                .Where(row =>
                                    row.Serial.DealDateTime >= date.MinDate && row.Serial.DealDateTime <= date.MaxDate
                                );
                        }
                    });
                }
                return queryable;
            }
            catch (Exception ex)
            {
                throw new Exception("MISApi.Services.CMS.Base.SerialService.DateQueryable", ex);
            }
        }
